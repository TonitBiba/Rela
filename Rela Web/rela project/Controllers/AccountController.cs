        [HttpPost]
        [AllowAnonymous]
        public async Task<IHttpActionResult> ResetPassword(ResetPasswordViewModel model)
        {
            if (!ModelState.IsValid)
            {
                return BadRequest("Model is not valid. Check it");
            }
            var user = await UserManager.FindByEmailAsync(model.Email);
            if (user == null)
            {
                return BadRequest("This user does not exist in the system. Check email again");
            }
            model.Code = model.Code.Replace(" ", "+");
            var result = await UserManager.ResetPasswordAsync(user.Id, model.Code, model.Password);
            if (result.Succeeded)
            {
                return Redirect(ConfigurationManager.AppSettings["url"] + "Home/ResetPasswordConfirmed");
            }
            else
            {
                return BadRequest();
            }
        }

